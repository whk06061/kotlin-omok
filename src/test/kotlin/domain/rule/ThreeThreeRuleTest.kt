//         4 ├──┼──┼──┼──┼──┼──┼──┼──┼──┼──┼──┼──┼──┼──┤
//         3 ├──┼──┼──┼──┼──┼──┼──┼──┼──┼──┼──┼──┼──┼──┤
//         2 ├──┼──┼──┼──┼──┼──┼──┼──┼──┼──┼──┼──┼──┼──┤
//         1 └──┴──┴──┴──┴──┴──┴──┴──┴──┴──┴──┴──┴──┴──┤
//            A  B  C  D  E  F  G  H  I  J  K  L  M  N  O
        val actual = ThreeThreeRule().checkRule(
            setOf(Stone('C', 12), Stone('E', 12), Stone('D', 13), Stone('D', 14)),
            setOf(Stone('I', 2), Stone('L', 4), Stone('I', 10), Stone('F', 12)), Stone('D', 12)
        )
        assertFalse(actual)
    }
}